#include <stdlib.h>
#include <stdio.h>

/**
* add - add two numbers
* @s: first number
* @d: second number
* Return: addition
*/

int add(int s, int d)
{
	return(s + d);
}

/**
* sub - subtract two numbers
* @s: first number
* @d: second number
* Return: subtraction
*/

int sub(int s, int d)
{
	return(s - d);
}

/**
* mul - multiple two numbers
* @s: first number
* @d: second number
* Return: multiplication
*/

int mul(int s, int d)
{
	return(s * d);
}

/**
* divd - divide two numbers
* @s: first number
* @d: second number
* Return: divition
*/

int divd(int s, int d)
{
	return(s / d);
}

/**
* mod - modulate two numbers
* @s: first number
* @d: second number
* Return: modulo
*/

int mod(int s, int d)
{
	return(s % d);
}
